package discult.modelapi.client.loaders.base;

/**
 * This class represents the texture coordinate locations of 
 * a model, and will be used with both static models and dynamic models.
 */
public class TextureCoords 
{
	public float u, v;

	public TextureCoords(float u, float v) {
		this.u = u;
		this.v = v;
	}
	
	
}
